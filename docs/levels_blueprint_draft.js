export function flattenCourseBlueprint() {
  const all = [];
  courseBlueprintDraft.forEach((chapter, chapterIndex) => {
    chapter.levels.forEach((level, levelIndex) => {
      all.push({
        ...level,
        chapterId: chapter.id,
        chapterTitle: chapter.title,
        chapterIndex,
        levelIndex,
      });
    });
  });
  return all;
}
