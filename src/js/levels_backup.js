/** 展平所有关卡为单一数组（带 chapterIdx 和 levelIdx） */
export function flattenLevels() {
  const all = [];
  chapters.forEach((ch, ci) => {
    ch.levels.forEach((lv, li) => {
      all.push({ ...lv, chapterIdx: ci, levelIdx: li });
    });
  });
  return all;
}
